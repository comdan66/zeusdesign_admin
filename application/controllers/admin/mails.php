class Mails extends Admin_controller {
  private $uri_1 = null;
  private $obj = null;
  private $icon = null;
  private $title = null;

  public function __construct () {
    parent::__construct ();
    
    if (!User::current ()->in_roles (array ('email')))
      return redirect_message (array ('admin'), array ('_fd' => '您的權限不足，或者頁面不存在。'));
    
    $this->uri_1 = 'admin/mails';
    $this->icon = 'icon-em';
    $this->title = 'E-Mail';

    if (in_array ($this->uri->rsegments (2, 0), array ('show')))
      if (!(($id = $this->uri->rsegments (3, 0)) && ($this->obj = Mail::find ('one', array ('conditions' => array ('id = ?', $id))))))
        return redirect_message (array ($this->uri_1), array ('_fd' => '找不到該筆資料。'));

    $this->add_param ('uri_1', $this->uri_1)
         ->add_param ('icon', $this->icon)
         ->add_param ('title', $this->title)
         ->add_param ('_url', base_url ($this->uri_1));
  }

  public function index ($offset = 0) {
    $searches = array (
        'to' => array ('el' => 'input', 'text' => '收件者', 'sql' => 'to LIKE ?'),
        'cc' => array ('el' => 'input', 'text' => '收件者(cc)', 'sql' => 'cc LIKE ?'),
        'title' => array ('el' => 'input', 'text' => '標題', 'sql' => 'title LIKE ?'),
        'content' => array ('el' => 'input', 'text' => '內容', 'sql' => 'content LIKE ?'),
      );

    $configs = array_merge (explode ('/', $this->uri_1), array ('%s'));
    $objs = conditions ($searches, $configs, $offset, 'Mail', array ('order' => 'id DESC'));

    UserLog::logRead (
      $this->icon,
      '檢視了' . $this->title . '列表',
      '搜尋條件細節可詢問工程師',
      $configs);

    return $this->load_view (array (
        'objs' => $objs,
        'total' => $offset,
        'searches' => $searches,
        'pagination' => $this->_get_pagination ($configs),
      ));
  }

  public function show () {
    UserLog::logRead ($this->icon, '檢視了一項' . $this->title);

    return $this->load_view (array (
        'obj' => $this->obj,
      ));
  }
}
